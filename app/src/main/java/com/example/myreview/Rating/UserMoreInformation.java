package com.example.myreview.Rating;

import android.net.Uri;

public class UserMoreInformation {


    private String phone, email, address, school, selfDescription, currentlyLookingFor;


    public UserMoreInformation(){
        this.phone = phone;
        this.email = email;
        this.address = address;
        this.school = school;
        this.selfDescription = selfDescription;
        this.currentlyLookingFor = currentlyLookingFor;



    }

    public String getPhone() {
        return phone;
    }

    public String getEmail() {
        return email;
    }

    public String getAddress() {
        return address;
    }

    public String getSchool() {
        return school;
    }

    public String getSelfDescription() {
        return selfDescription;
    }

    public String getCurrentlyLookingFor() {
        return currentlyLookingFor;
    }
}
